#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*mem;

	if (size != 0 && nmemb > SIZE_MAX / size)
		return (NULL);
	if (nmemb == 0 || size == 0)
		return (malloc(0));
	mem = malloc(nmemb * size);
	if (mem == NULL)
		return (NULL);
	ft_memset(mem, 0, nmemb * size);
	return (mem);
}
